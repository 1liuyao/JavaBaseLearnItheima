package com.itheima_01;

import java.text.ParseException;
import java.text.SimpleDateFormat;
import java.util.Date;

/*
    SimpleDateFormat类是【abstract】DateFormat类的一个具体子类，用于格式化日期或时间。

    构造方法：
        public SimpleDateFormat():构造一个SimpleDateFormat，使用默认模式和日期格式
        public SimpleDateFormatString pattern):构造一个SimpleDateFormat使用给定的模式和默认的日期格式

    格式化：从 Date 到 String
        public final String format(Date date)：将日期格式化成日期/时间字符串

    解析：从 String 到 Date
        public Date parse(String source)：从给定字符串的开始解析文本以生成日期
 */
public class SimpleDateFormatDemo {
    public static void main(String[] args) throws ParseException {
        //格式化：从 Date 到 String
        Date d = new Date();
  //      SimpleDateFormat sdf = new SimpleDateFormat();//22-7-26 下午3:01
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy年MM月dd日 HH:mm:ss");
        String s = sdf.format(d);
        System.out.println(s);
        System.out.println("--------");

        //从 String 到 Date，pattern得和String对上
        String ss = "2048-08-09 11:11:11";
        //ParseException ALT+Enter选择解决方法
        SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date dd = sdf2.parse(ss);
        System.out.println(dd);
    }
}
